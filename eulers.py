def f(x, y):
    return x + y

x0 = 0
y0 = 1
h = 0.1
xn = 0.5

print("------------------------------------------------------------")
print("                     EULER'S METHOD")
print("------------------------------------------------------------")
print(" x0\t\t   y0\t\t   f(x,y)\t   y_new")
print("------------------------------------------------------------")

while x0 < xn:
    fxy = f(x0, y0)
    y1 = y0 + h * fxy
    print(f" {x0:<8.2f}\t {y0:<10.6f}\t {fxy:<10.6f}\t {y1:<10.6f}")
    x0 += h
    y0 = y1

print("------------------------------------------------------------")
print(f" Approximate value of y at x = {xn} is {y0:.6f}")
print(f" Rounded to 4 decimal places : {round(y0,4)}")
print("------------------------------------------------------------")
